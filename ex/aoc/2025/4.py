def is_accessible(x, y, text: str) -> bool:
    lines = text.splitlines()
    curr_char = lines[x][y]

    if curr_char != "@":
        return False

    adjacent = []

    for dx in [-1, 0, 1]:
        for dy in [-1, 0, 1]:
            if dx == 0 and dy == 0:
                continue

            nx, ny = x + dx, y + dy

            if 0 <= nx < len(lines) and 0 <= ny < len(lines[nx]):
                adjacent.append(lines[nx][ny])

    return adjacent.count("@") < 4


def t1():
    count = 0
    text = TEXT
    for index, line in enumerate(text.splitlines()):
        for index2, char in enumerate(line):
            if is_accessible(index, index2, text):
                count += 1

    print(count)


def count_adjacent_rolls(x, y, grid):
    count = 0
    for dx in [-1, 0, 1]:
        for dy in [-1, 0, 1]:
            if dx == 0 and dy == 0:
                continue
            nx, ny = x + dx, y + dy
            if 0 <= nx < len(grid) and 0 <= ny < len(grid[nx]):
                if grid[nx][ny] == "@":
                    count += 1
    return count


def find_accessible_rolls(grid):
    accessible = []
    for x in range(len(grid)):
        for y in range(len(grid[x])):
            if grid[x][y] == "@":
                if count_adjacent_rolls(x, y, grid) < 4:
                    accessible.append((x, y))
    return accessible


def t2(text: str) -> int:
    grid = [list(line) for line in text.strip().split("\n")]

    total_removed = 0

    while True:
        accessible = find_accessible_rolls(grid)

        if not accessible:
            break

        for x, y in accessible:
            grid[x][y] = "."

        total_removed += len(accessible)

    print(total_removed)


